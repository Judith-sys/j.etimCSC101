fn main() {
    // Variable assignments
    let num1 = 10; // Corrected variable name and assignment
    let num2 = 5;  // Initialize num2 with a value

    // Initialize result for different operations
    let mut result: i32;

    // Sum
    result = num1 + num2;
    println!("Sum: {}", result);

    // Difference
    result = num1 - num2;
    println!("Difference: {}", result);

    // Product
    result = num1 * num2;
    println!("Product: {}", result);

    // Quotient
    result = num1 / num2;
    println!("Quotient: {}", result);

    // Remainder (modulus)
    result = num1 % num2;
    println!("Remainder: {}", result);
}
