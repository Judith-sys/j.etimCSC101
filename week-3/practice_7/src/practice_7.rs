fn main() {
    // Define the float with the correct format
    let float_with_separator = 11000.555001; // No spaces in the number

    // Print the float value
    println!("Float value: {}", float_with_separator);

    // Define the int with the correct format
    let int_with_separator = 50_000; // Underscores are allowed in Rust

    // Print the int value
    println!("Int value: {}", int_with_separator);
}
