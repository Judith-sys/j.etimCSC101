fn main() {
    // Addition
    let sum = 5550 + 7310;
    println!("The sum of 5550 and 7310 is {}", sum);

    // Subtraction
    let difference: f32 = 95.5 - 4.3;
    println!("The difference of 95.5 and 4.3 is {}", difference);

    // Multiplication
    let product: f32 = 4.0 * 30.0; // Using f32 for decimal numbers
    println!("The product of 4 and 30 is {}", product);

    // Division
    let quotient: f32 = 100.0 / 3.0;
    println!("The quotient of 100 and 3 is {}", quotient);
}
