fn main() {
  // String type
  let school_string = "School of Science and Technology";

  // Float type
  let rating: f32 = 5.0;

  // Boolean type
  let is_growing: bool = true;

  // Integer type
  let department_count: i32 = 3;

  // Printing the values
  println!("School Name is: {}", school_string);
  println!("School rating on 5 is: {}", rating);
  println!("School is growing: {}", is_growing);
  println!("Number of Departments: {}", department_count);
}